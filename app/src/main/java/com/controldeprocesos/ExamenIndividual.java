package com.controldeprocesos;

public class ExamenIndividual {
    private int idExamenIndividual;
    private int numeva;
    private String carnet;
    private float nota;
    public int getIdExamenIndividual() {
        return idExamenIndividual;
    }

    public void setIdExamenIndividual(int idExamenIndividual) {
        this.idExamenIndividual = idExamenIndividual;
    }

    public int getNumeva() {
        return numeva;
    }

    public void setNumeva(int numeva) {
        this.numeva = numeva;
    }

    public String getCarnet() {
        return carnet;
    }

    public void setCarnet(String carnet) {
        this.carnet = carnet;
    }

    public float getNota() {
        return nota;
    }

    public void setNota(float nota) {
        this.nota = nota;
    }




}
