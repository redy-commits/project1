package com.controldeprocesos;

public class SegundaRevision {
    private int numRev2;
    private int idExamen;
    private String respSociedadEstud;
    private int idDocente;
    private int idOtroDocente;
    private float notaDefinitiva;

    public int getNumRev2() {
        return numRev2;
    }

    public void setNumRev2(int numRev2) {
        this.numRev2 = numRev2;
    }

    public int getIdExamen() {
        return idExamen;
    }

    public void setIdExamen(int idExamen) {
        this.idExamen = idExamen;
    }

    public String getRespSociedadEstud() {
        return respSociedadEstud;
    }

    public void setRespSociedadEstud(String respSociedadEstud) {
        this.respSociedadEstud = respSociedadEstud;
    }

    public int getIdDocente() {
        return idDocente;
    }

    public void setIdDocente(int idDocente) {
        this.idDocente = idDocente;
    }

    public int getIdOtroDocente() {
        return idOtroDocente;
    }

    public void setIdOtroDocente(int idOtroDocente) {
        this.idOtroDocente = idOtroDocente;
    }

    public float getNotaDefinitiva() {
        return notaDefinitiva;
    }

    public void setNotaDefinitiva(float notaDefinitiva) {
        this.notaDefinitiva = notaDefinitiva;
    }


}
